ps = PorterStemmer()

def formatString(input):
    # Split and remove all stop words
    input = input.split(' ')
    input = [word for word in input if word not in stopwordlist]

    # Change words to be singular and breaks down words in different tenses
    output = []
    for word in input:
        output.append(ps.stem(word))
    
    output = ' '.join(output)
    return output

# Formats a given file with the lyric string
def formatFile(filepath):
    inputString = ''
    with open(filepath, 'r') as f:
        inputString = f.read()
    
    with open(filepath, 'w') as f:
        f.write(formatString(inputString))

